"""
lemmatizer.py
=============
Лемматизатор на pymorphy3 — используется и при обучении (train_model.py)
и при загрузке модели (intent_classifier.py).
Вынесен в отдельный модуль, чтобы joblib мог найти LemmaTransformer при десериализации.
"""

import pymorphy3
from sklearn.base import BaseEstimator, TransformerMixin

_morph = pymorphy3.MorphAnalyzer()

STOP_WORDS = {
    'и','в','во','не','что','он','на','я','с','со','как','а','то','все','она',
    'так','его','но','да','ты','к','у','же','вы','за','бы','по','только','её',
    'мне','было','вот','от','меня','ещё','нет','о','из','ему','теперь','когда',
    'даже','ну','вдруг','ли','если','уже','или','ни','быть','был','него','до',
    'вас','нибудь','опять','уж','вам','ведь','там','потом','себя','ничего',
    'ей','может','они','тут','где','есть','надо','ней','для','мы','тебя','их',
    'чем','была','сам','чтоб','без','будто','чего','раз','тоже','себе','под',
    'будет','ж','тогда','кто','этот','того','потому','этого','какой','совсем',
    'ним','здесь','этом','один','почти','мой','тем','чтобы','нее','сейчас',
    'были','куда','зачем','всех','никогда','можно','при','наконец','два',
    'об','другой','хоть','после','над','больше','тот','через','эти','нас',
    'про','всего','них','какая','много','разве','три','эту','моя','впрочем',
    'хорошо','свою','этой','перед','иногда','лучше','чуть','том','нельзя',
    'такой','им','более','всегда','конечно','всю','между',
}


def lemmatize(text: str) -> str:
    words = text.lower().split()
    result = []
    for word in words:
        clean = ''.join(c for c in word if c.isalpha() or c.isdigit())
        if not clean or clean in STOP_WORDS:
            continue
        parsed = _morph.parse(clean)
        lemma = parsed[0].normal_form if parsed else clean
        result.append(lemma)
    return ' '.join(result)


class LemmaTransformer(BaseEstimator, TransformerMixin):
    def fit(self, X, y=None):
        return self
    def transform(self, X):
        return [lemmatize(text) for text in X]
